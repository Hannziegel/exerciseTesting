export class Calculator {

  static add = (num1, num2) => {
    const result = num1 + num2;
    return result;
  };

  static subtract = (num1, num2) => {
    const result = num1 - num2;
    return result;
  }

  static divide = (num1, num2) => {
    const result = num1 / num2;
    return result;
  }

  static multiply = (num1, num2) => {
    const result = num1 * num2;
    return result;
  }
}

